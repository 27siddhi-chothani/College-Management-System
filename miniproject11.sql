-- to create student, course, faculty, attendence, results and course_faculty tables

CREATE TABLE STUDE(
STUDENT_ID INT,
STUDENT_NAME VARCHAR(50),
DEPARTMENT VARCHAR(50),
YEAR INT
);

CREATE TABLE COURSE(
COURSE_ID INT,
COURSE_NAME VARCHAR(50),
CREDITS INT
);

CREATE TABLE FACULTY(
FACULTY_ID INT,
FACULTY_NAME VARCHAR(50),
DEPARTMENT VARCHAR(50)
);

CREATE TABLE ATTEN(
ATTENDENCE_ID INT,
STUDENT_ID INT,
COURSE_ID INT,
ATTENDED_CLASSES INT,
TOTAL_CLASSES INT
);

CREATE TABLE RESULTS(
RESULT_ID INT,
STUDENT_ID INT,
COURSE_ID INT,
MARKS FLOAT,
GRADE CHAR(2)
);

CREATE TABLE COURSE_FACULTY(
COURSE_ID INT,
FACULTY_ID INT
);

