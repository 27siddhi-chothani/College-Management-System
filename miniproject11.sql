-- TO SLOVE IMPORTANT QUERIES

-- 1. view all students
SELECT * FROM STUDE;

-- 2. top performing students
SELECT STUDENT_NAME, MARKS 
FROM STUDE AS S
INNER JOIN RESULTS AS R
ON S.STUDENT_ID = R.STUDENT_ID
ORDER BY MARKS DESC;

-- 3. attendence percentage
SELECT STUDENT_NAME, COURSE_NAME, (ATTENDED_CLASSES * 100/ TOTAL_CLASSES) AS ATTENDENCE_PERCENTAGE
FROM STUDE AS S
INNER JOIN ATTEN AS A
ON A.STUDENT_ID = S.STUDENT_ID
INNER JOIN COURSE AS C
ON A.COURSE_ID = C.COURSE_ID;

-- 4. students below 75% attendence
SELECT STUDENT_NAME, (ATTENDED_CLASSES * 100/ TOTAL_CLASSES) AS ATTENDENCE_PERCENTAGE
FROM STUDE AS S
INNER JOIN ATTEN AS A
ON S.STUDENT_ID = A.STUDENT_ID
WHERE (ATTENDED_CLASSES * 100/ TOTAL_CLASSES) < 75;

-- 5. course enrollement analysis
SELECT COURSE_NAME, COUNT(STUDENT_ID) AS TOTAL_STUDENT
FROM COURSE AS C
INNER JOIN ATTEN AS A
ON A.COURSE_ID = C.COURSE_ID
GROUP BY COURSE_NAME;

-- 6. faculty worklod
SELECT FACULTY_NAME, COUNT(COURSE_ID) AS TOTAL_COURSE
FROM FACULTY AS F
INNER JOIN COURSE_FACULTY AS CF
ON F.FACULTY_ID = CF.FACULTY_ID
GROUP BY FACULTY_NAME;

-- 7. nested query - students above avg marks 
SELECT STUDENT_NAME FROM STUDE
WHERE STUDENT_ID IN(
    SELECT STUDENT_ID FROM RESULTS 
    WHERE MARKS > (
        SELECT AVG(MARKS) FROM RESULTS)
        );

-- 8. create view - students performance summary
CREATE VIEW STUDENT_PERFORMANCE AS
SELECT STUDENT_NAME, COURSE_NAME, MARKS, GRADE
FROM STUDE AS S
INNER JOIN RESULTS AS R
ON S.STUDENT_ID = R.STUDENT_ID
INNER JOIN COURSE AS C
ON R.COURSE_ID = C.COURSE_ID;

SELECT * FROM STUDENT_PERFORMANCE;







